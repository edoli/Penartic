        assert_eq!(plan.gcode_lines[11], "G90");
        assert_eq!(plan.gcode_lines[12], "G1 X40.00 Y10.00 F1500");
        assert_eq!(plan.first_draw_point, Some(vec2(10.0, 10.0)));
