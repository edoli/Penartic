use std::f32::consts::TAU;

use glam::{Vec2, vec2, vec3};

use crate::{
    plot::model::{
        CurveOutputMode, MotionKind, MotionSegment, PrintStartMode, ToolSettings, ToolpathPlan,
        ToolpathStats,
    },
    svg::{
        ir::{CubicBezierSegment, LineSegment, SvgIrSegment, SvgIrStroke},
        toolpath::PreparedSvg,
    },
};

const ARC_PREVIEW_SEGMENT_LENGTH_MM: f32 = 2.0;
const MIN_ARC_PREVIEW_SUBDIVISIONS: usize = 4;
const MAX_ARC_PREVIEW_SUBDIVISIONS: usize = 96;
const CORNER_EPSILON: f32 = 1e-4;
const MAX_SMOOTHABLE_CORNER_TURN_DEG: f32 = 170.0;

#[derive(Debug, Clone, Copy)]
enum DrawPrimitive {
    Ir(SvgIrSegment),
    Arc(ArcSegment),
}

impl DrawPrimitive {
    fn start_point(self) -> Vec2 {
        match self {
            Self::Ir(segment) => segment.start_point(),
            Self::Arc(segment) => segment.start,
        }
    }

    fn flatten_points(self) -> Vec<Vec2> {
        match self {
            Self::Ir(segment) => segment.flatten_points(),
            Self::Arc(segment) => segment.flatten_points(),
        }
    }

    fn to_cubic_bezier(self) -> Option<CubicBezierSegment> {
        match self {
            Self::Ir(segment) => segment.to_cubic_bezier(),
            Self::Arc(_) => None,
        }
    }

    fn is_arc(self) -> bool {
        matches!(self, Self::Arc(_))
    }
}

#[derive(Debug, Clone, Copy)]
struct ArcSegment {
    start: Vec2,
    end: Vec2,
    center: Vec2,
    clockwise: bool,
}

impl ArcSegment {
    fn radius(self) -> f32 {
        self.start.distance(self.center)
    }

    fn signed_sweep_radians(self) -> f32 {
        let start_vector = self.start - self.center;
        let end_vector = self.end - self.center;
        let sweep = cross_2d(start_vector, end_vector).atan2(start_vector.dot(end_vector));

        if self.clockwise {
            if sweep > 0.0 { sweep - TAU } else { sweep }
        } else if sweep < 0.0 {
            sweep + TAU
        } else {
            sweep
        }
    }

    fn approximate_length(self) -> f32 {
        self.radius() * self.signed_sweep_radians().abs()
    }

    fn flatten_points(self) -> Vec<Vec2> {
        let radius = self.radius();
        let sweep = self.signed_sweep_radians();
        if radius <= CORNER_EPSILON || sweep.abs() <= CORNER_EPSILON {
            return vec![self.start, self.end];
        }

        let steps = ((self.approximate_length() / ARC_PREVIEW_SEGMENT_LENGTH_MM).ceil() as usize)
            .clamp(MIN_ARC_PREVIEW_SUBDIVISIONS, MAX_ARC_PREVIEW_SUBDIVISIONS);
        let start_angle = (self.start.y - self.center.y).atan2(self.start.x - self.center.x);

        let mut points = Vec::with_capacity(steps + 1);
        points.push(self.start);

        for step in 1..=steps {
            let t = step as f32 / steps as f32;
            let angle = start_angle + sweep * t;
            points.push(self.center + vec2(angle.cos(), angle.sin()) * radius);
        }

        if let Some(last) = points.last_mut() {
            *last = self.end;
        }

        points
    }
}

pub fn build_plan(prepared: PreparedSvg, settings: &ToolSettings) -> ToolpathPlan {
    let PreparedSvg {
        source_name,
        strokes,
        warnings,
        drawing_origin,
        drawing_bounds,
        is_out_of_bounds,
    } = prepared;

    let stroke_primitives = strokes
        .iter()
        .map(|stroke| build_draw_primitives(stroke, settings))
        .filter(|primitives| !primitives.is_empty())
        .collect::<Vec<_>>();

    let mut segments = Vec::new();
    let mut segment_end_times_s = Vec::new();
    let mut gcode_lines = Vec::new();

    let draw_feed = settings.print_feed_rate();
    let travel_feed = settings.travel_feed_rate();
    let draw_speed = (draw_feed / 60.0).max(1.0);
    let travel_speed = (travel_feed / 60.0).max(1.0);
    let lift = settings.lift_height_mm;
    let first_draw_point = stroke_primitives
        .iter()
        .find_map(|primitives| primitives.first().map(|primitive| primitive.start_point()));
    let has_arc_primitives = stroke_primitives
        .iter()
        .flat_map(|primitives| primitives.iter())
        .any(|primitive| primitive.is_arc());
    let has_bezier_primitives = strokes
        .iter()
        .flat_map(|stroke| stroke.segments.iter())
        .any(|segment| matches!(segment, SvgIrSegment::Quadratic(_) | SvgIrSegment::Cubic(_)));

    gcode_lines.push("; Generated by Penartic".to_owned());
    gcode_lines.push(format!("; Source: {}", source_name));
    gcode_lines.push("G21".to_owned());
    gcode_lines.push("G90".to_owned());

    let mut current = match settings.print_start_mode {
        PrintStartMode::HomeBeforePrint => {
            let mut current = vec3(0.0, 0.0, 0.0);
            let lifted_origin = vec3(0.0, 0.0, lift);
            push_relative_z_motion_if_needed(
                &mut segments,
                &mut segment_end_times_s,
                &mut gcode_lines,
                &mut current,
                lifted_origin,
                travel_speed,
                travel_feed,
            );

            gcode_lines.push("G28 X Y".to_owned());
            lifted_origin
        }
        PrintStartMode::DirectFromCurrentPosition => first_draw_point
            .map(|point| vec3(point.x, point.y, 0.0))
            .unwrap_or_else(|| vec3(0.0, 0.0, 0.0)),
    };

    let stroke_count = stroke_primitives.len();
    let mut active_feed_rate = None;
    let mut is_first_stroke = true;

    for primitives in &stroke_primitives {
        let start_draw = primitives
            .first()
            .map(|primitive| primitive.start_point())
            .map(|point| vec3(point.x, point.y, 0.0))
            .unwrap();
        if settings.print_start_mode == PrintStartMode::HomeBeforePrint || !is_first_stroke {
            push_position_to_stroke_start(
                &mut segments,
                &mut segment_end_times_s,
                &mut gcode_lines,
                &mut current,
                start_draw,
                lift,
                travel_speed,
                travel_feed,
                &mut active_feed_rate,
            );
        } else {
            debug_assert!(current.distance_squared(start_draw) <= 1e-6);
            current = start_draw;
        }

        for primitive in primitives {
            append_draw_primitive(
                &mut segments,
                &mut segment_end_times_s,
                &mut gcode_lines,
                &mut current,
                *primitive,
                settings.curve_output_mode,
                draw_speed,
                draw_feed,
                &mut active_feed_rate,
            );
        }

        let raised = vec3(current.x, current.y, lift);
        push_relative_z_motion_if_needed(
            &mut segments,
            &mut segment_end_times_s,
            &mut gcode_lines,
            &mut current,
            raised,
            travel_speed,
            travel_feed,
        );
        active_feed_rate = None;
        is_first_stroke = false;
    }

    gcode_lines.push("M400".to_owned());

    let mut stats =
        ToolpathStats { stroke_count, segment_count: segments.len(), ..Default::default() };

    for segment in &segments {
        match segment.kind {
            MotionKind::Travel => {
                stats.travel_distance_mm += segment.length();
                stats.estimated_duration_s += segment.duration_s;
            }
            MotionKind::Draw => {
                stats.drawing_distance_mm += segment.length();
                stats.estimated_duration_s += segment.duration_s;
            }
        }
    }

    let mut warnings = warnings;
    if settings.curve_output_mode.prefers_g2g3() && has_arc_primitives {
        warnings.push(
            "G2/G3 원호 출력은 지원 펌웨어에서만 올바르게 동작합니다. 지원하지 않으면 원호 출력을 끄세요."
                .to_owned(),
        );
    }
    if settings.curve_output_mode.prefers_g5() && has_bezier_primitives {
        warnings.push(
            "G5 곡선 출력은 지원 펌웨어에서만 올바르게 동작합니다. 지원하지 않으면 선분 출력 모드를 사용하세요."
                .to_owned(),
        );
    }

    ToolpathPlan {
        source_name,
        printable_area: settings.printable_area,
        drawing_origin,
        drawing_bounds,
        first_draw_point,
        is_out_of_bounds,
        segments,
        segment_end_times_s,
        gcode_lines,
        warnings,
        stats,
    }
}

fn build_draw_primitives(stroke: &SvgIrStroke, settings: &ToolSettings) -> Vec<DrawPrimitive> {
    let mut primitives = Vec::new();
    let mut line_run = Vec::new();

    for segment in &stroke.segments {
        match *segment {
            SvgIrSegment::Line(segment) => {
                if line_run.is_empty() {
                    line_run.push(segment.start);
                    line_run.push(segment.end);
                } else if points_match(*line_run.last().unwrap(), segment.start) {
                    line_run.push(segment.end);
                } else {
                    flush_line_run(&mut line_run, settings, &mut primitives);
                    line_run.push(segment.start);
                    line_run.push(segment.end);
                }
            }
            _ => {
                flush_line_run(&mut line_run, settings, &mut primitives);
                primitives.push(DrawPrimitive::Ir(*segment));
            }
        }
    }

    flush_line_run(&mut line_run, settings, &mut primitives);
    primitives
}

fn flush_line_run(
    line_run: &mut Vec<Vec2>,
    settings: &ToolSettings,
    primitives: &mut Vec<DrawPrimitive>,
) {
    if line_run.len() >= 2 {
        primitives.extend(smooth_polyline_run(line_run, settings));
    }
    line_run.clear();
}

fn smooth_polyline_run(points: &[Vec2], settings: &ToolSettings) -> Vec<DrawPrimitive> {
    if points.len() < 2 {
        return Vec::new();
    }

    let is_closed = points.len() >= 4 && points_match(points[0], *points.last().unwrap());
    if is_closed {
        let unique_points = &points[..points.len() - 1];
        if unique_points.len() >= 3 {
            return build_smoothed_closed_polyline(unique_points, settings);
        }
    }

    build_smoothed_open_polyline(points, settings)
}

fn build_smoothed_open_polyline(points: &[Vec2], settings: &ToolSettings) -> Vec<DrawPrimitive> {
    let mut primitives = Vec::new();
    let trims = compute_corner_trims(points, false, settings);
    let mut current = points[0];

    for index in 1..points.len() - 1 {
        let corner = points[index];
        if trims[index] <= CORNER_EPSILON {
            push_line_primitive(&mut primitives, current, corner);
            current = corner;
            continue;
        }

        if let Some((tangent_start, arc)) =
            corner_transition(points[index - 1], corner, points[index + 1], trims[index])
        {
            push_line_primitive(&mut primitives, current, tangent_start);
            primitives.push(DrawPrimitive::Arc(arc));
            current = arc.end;
        } else {
            push_line_primitive(&mut primitives, current, corner);
            current = corner;
        }
    }

    push_line_primitive(&mut primitives, current, *points.last().unwrap());
    primitives
}

fn build_smoothed_closed_polyline(points: &[Vec2], settings: &ToolSettings) -> Vec<DrawPrimitive> {
    let mut primitives = Vec::new();
    let trims = compute_corner_trims(points, true, settings);

    let start_transition =
        corner_transition(points[points.len() - 1], points[0], points[1], trims[0]);
    let mut current = start_transition.map(|(_, arc)| arc.end).unwrap_or(points[0]);

    for step in 1..=points.len() {
        let index = step % points.len();
        let previous = if index == 0 { points.len() - 1 } else { index - 1 };
        let next = (index + 1) % points.len();

        if let Some((tangent_start, arc)) =
            corner_transition(points[previous], points[index], points[next], trims[index])
        {
            push_line_primitive(&mut primitives, current, tangent_start);
            primitives.push(DrawPrimitive::Arc(arc));
            current = arc.end;
        } else {
            push_line_primitive(&mut primitives, current, points[index]);
            current = points[index];
        }
    }

    primitives
}

fn compute_corner_trims(points: &[Vec2], closed: bool, settings: &ToolSettings) -> Vec<f32> {
    let mut trims = vec![0.0; points.len()];
    if !settings.corner_smoothing_enabled
        || settings.corner_smoothing_radius_mm <= CORNER_EPSILON
        || points.len() < 3
    {
        return trims;
    }

    let min_turn_radians = settings.corner_smoothing_angle_deg.to_radians();
    let max_turn_radians = MAX_SMOOTHABLE_CORNER_TURN_DEG.to_radians();

    for index in 0..points.len() {
        if !closed && (index == 0 || index + 1 == points.len()) {
            continue;
        }

        let previous = if index == 0 { points.len() - 1 } else { index - 1 };
        let next = if index + 1 == points.len() { 0 } else { index + 1 };
        let incoming = points[index] - points[previous];
        let outgoing = points[next] - points[index];
        let incoming_length = incoming.length();
        let outgoing_length = outgoing.length();
        if incoming_length <= CORNER_EPSILON || outgoing_length <= CORNER_EPSILON {
            continue;
        }

        let incoming_direction = incoming / incoming_length;
        let outgoing_direction = outgoing / outgoing_length;
        let turn = incoming_direction.dot(outgoing_direction).clamp(-1.0, 1.0).acos();
        if turn < min_turn_radians || turn > max_turn_radians {
            continue;
        }

        if cross_2d(incoming_direction, outgoing_direction).abs() <= CORNER_EPSILON {
            continue;
        }

        let tan_half_turn = (turn * 0.5).tan();
        if !tan_half_turn.is_finite() || tan_half_turn <= CORNER_EPSILON {
            continue;
        }

        trims[index] = settings.corner_smoothing_radius_mm * tan_half_turn;
    }

    limit_corner_trims(points, closed, &mut trims);
    trims
}

fn limit_corner_trims(points: &[Vec2], closed: bool, trims: &mut [f32]) {
    if points.len() < 2 {
        return;
    }

    let segment_count = if closed { points.len() } else { points.len() - 1 };
    for _ in 0..segment_count.saturating_mul(4).max(1) {
        let mut changed = false;

        for segment_index in 0..segment_count {
            let start_index = segment_index;
            let end_index =
                if closed { (segment_index + 1) % points.len() } else { segment_index + 1 };
            let segment_length = points[start_index].distance(points[end_index]);
            if segment_length <= CORNER_EPSILON {
                trims[start_index] = 0.0;
                trims[end_index] = 0.0;
                continue;
            }

            let available_length = (segment_length - CORNER_EPSILON).max(0.0);
            let total_trim = trims[start_index] + trims[end_index];
            if total_trim <= available_length || total_trim <= CORNER_EPSILON {
                continue;
            }

            let scale = available_length / total_trim;
            trims[start_index] *= scale;
            trims[end_index] *= scale;
            changed = true;
        }

        if !changed {
            break;
        }
    }
}

fn corner_transition(
    previous: Vec2,
    corner: Vec2,
    next: Vec2,
    trim: f32,
) -> Option<(Vec2, ArcSegment)> {
    if trim <= CORNER_EPSILON {
        return None;
    }

    let incoming = corner - previous;
    let outgoing = next - corner;
    let incoming_length = incoming.length();
    let outgoing_length = outgoing.length();
    if incoming_length <= CORNER_EPSILON || outgoing_length <= CORNER_EPSILON {
        return None;
    }

    let incoming_direction = incoming / incoming_length;
    let outgoing_direction = outgoing / outgoing_length;
    let turn = incoming_direction.dot(outgoing_direction).clamp(-1.0, 1.0).acos();
    let tan_half_turn = (turn * 0.5).tan();
    if !tan_half_turn.is_finite() || tan_half_turn <= CORNER_EPSILON {
        return None;
    }

    let radius = trim / tan_half_turn;
    if !radius.is_finite() || radius <= CORNER_EPSILON {
        return None;
    }

    let start = corner - incoming_direction * trim;
    let end = corner + outgoing_direction * trim;
    let turn_cross = cross_2d(incoming_direction, outgoing_direction);
    if turn_cross.abs() <= CORNER_EPSILON {
        return None;
    }

    let normal = if turn_cross > 0.0 {
        left_normal(incoming_direction)
    } else {
        right_normal(incoming_direction)
    };
    let outgoing_normal = if turn_cross > 0.0 {
        left_normal(outgoing_direction)
    } else {
        right_normal(outgoing_direction)
    };
    let center_from_incoming = start + normal * radius;
    let center_from_outgoing = end + outgoing_normal * radius;
    let center = (center_from_incoming + center_from_outgoing) * 0.5;

    Some((start, ArcSegment { start, end, center, clockwise: turn_cross < 0.0 }))
}

fn push_line_primitive(primitives: &mut Vec<DrawPrimitive>, start: Vec2, end: Vec2) {
    if start.distance_squared(end) <= CORNER_EPSILON * CORNER_EPSILON {
        return;
    }

    primitives.push(DrawPrimitive::Ir(SvgIrSegment::Line(LineSegment { start, end })));
}

fn points_match(a: Vec2, b: Vec2) -> bool {
    a.distance_squared(b) <= CORNER_EPSILON * CORNER_EPSILON
}

fn cross_2d(a: Vec2, b: Vec2) -> f32 {
    a.x * b.y - a.y * b.x
}

fn left_normal(direction: Vec2) -> Vec2 {
    vec2(-direction.y, direction.x)
}

fn right_normal(direction: Vec2) -> Vec2 {
    vec2(direction.y, -direction.x)
}

fn append_draw_primitive(
    segments: &mut Vec<MotionSegment>,
    segment_end_times_s: &mut Vec<f32>,
    gcode_lines: &mut Vec<String>,
    current: &mut glam::Vec3,
    primitive: DrawPrimitive,
    curve_output_mode: CurveOutputMode,
    draw_speed: f32,
    draw_feed: f32,
    active_feed_rate: &mut Option<i32>,
) {
    let points = primitive.flatten_points();
    for window in points.windows(2) {
        let next = vec3(window[1].x, window[1].y, 0.0);
        if current.distance_squared(next) <= 1e-6 {
            continue;
        }
        push_segment(segments, segment_end_times_s, *current, next, MotionKind::Draw, draw_speed);
        *current = next;
    }

    match primitive {
        DrawPrimitive::Arc(segment) if curve_output_mode.prefers_g2g3() => {
            push_g2g3_move(gcode_lines, active_feed_rate, draw_feed, segment);
        }
        _ if curve_output_mode.prefers_g5() => {
            if let Some(cubic) = primitive.to_cubic_bezier() {
                push_g5_move(gcode_lines, active_feed_rate, draw_feed, cubic);
                return;
            }

            for point in points.into_iter().skip(1) {
                push_g1_move(
                    gcode_lines,
                    active_feed_rate,
                    draw_feed,
                    Some(point.x),
                    Some(point.y),
                    None,
                );
            }
        }
        _ => {
            for point in points.into_iter().skip(1) {
                push_g1_move(
                    gcode_lines,
                    active_feed_rate,
                    draw_feed,
                    Some(point.x),
                    Some(point.y),
                    None,
                );
            }
        }
    }
}

fn push_position_to_stroke_start(
    segments: &mut Vec<MotionSegment>,
    segment_end_times_s: &mut Vec<f32>,
    gcode_lines: &mut Vec<String>,
    current: &mut glam::Vec3,
    start_draw: glam::Vec3,
    lift: f32,
    travel_speed: f32,
    travel_feed: f32,
    active_feed_rate: &mut Option<i32>,
) {
    let lifted_current = vec3(current.x, current.y, lift);
    push_relative_z_motion_if_needed(
        segments,
        segment_end_times_s,
        gcode_lines,
        current,
        lifted_current,
        travel_speed,
        travel_feed,
    );

    let start_lifted = vec3(start_draw.x, start_draw.y, lift);
    if current.distance_squared(start_lifted) > 1e-6 {
        push_segment(
            segments,
            segment_end_times_s,
            *current,
            start_lifted,
            MotionKind::Travel,
            travel_speed,
        );
        push_g1_move(
            gcode_lines,
            active_feed_rate,
            travel_feed,
            Some(start_lifted.x),
            Some(start_lifted.y),
            None,
        );
        *current = start_lifted;
    }

    push_relative_z_motion_if_needed(
        segments,
        segment_end_times_s,
        gcode_lines,
        current,
        start_draw,
        travel_speed,
        travel_feed,
    );
    *active_feed_rate = None;
}

fn push_relative_z_motion_if_needed(
    segments: &mut Vec<MotionSegment>,
    segment_end_times_s: &mut Vec<f32>,
    gcode_lines: &mut Vec<String>,
    current: &mut glam::Vec3,
    end: glam::Vec3,
    speed_mm_s: f32,
    feed_rate_mm_min: f32,
) {
    if current.distance_squared(end) <= 1e-6 {
        return;
    }

    debug_assert!((current.x - end.x).abs() <= 1e-6);
    debug_assert!((current.y - end.y).abs() <= 1e-6);

    push_segment(segments, segment_end_times_s, *current, end, MotionKind::Travel, speed_mm_s);
    gcode_lines.push("G91".to_owned());
    gcode_lines.push(format!("G1 Z{:.3} F{:.0}", end.z - current.z, feed_rate_mm_min.round()));
    gcode_lines.push("G90".to_owned());
    *current = end;
}

fn push_g1_move(
    gcode_lines: &mut Vec<String>,
    active_feed_rate: &mut Option<i32>,
    feed_rate_mm_min: f32,
    x: Option<f32>,
    y: Option<f32>,
    z: Option<f32>,
) {
    let mut line = "G1".to_owned();

    if let Some(x) = x {
        line.push_str(&format!(" X{x:.2}"));
    }
    if let Some(y) = y {
        line.push_str(&format!(" Y{y:.2}"));
    }
    if let Some(z) = z {
        line.push_str(&format!(" Z{z:.3}"));
    }

    let rounded_feed_rate = feed_rate_mm_min.round() as i32;
    if active_feed_rate.is_none_or(|active| active != rounded_feed_rate) {
        line.push_str(&format!(" F{rounded_feed_rate}"));
        *active_feed_rate = Some(rounded_feed_rate);
    }

    gcode_lines.push(line);
}

fn push_g2g3_move(
    gcode_lines: &mut Vec<String>,
    active_feed_rate: &mut Option<i32>,
    feed_rate_mm_min: f32,
    arc: ArcSegment,
) {
    let command = if arc.clockwise { "G2" } else { "G3" };
    let mut line = format!(
        "{command} X{:.2} Y{:.2} I{:.3} J{:.3}",
        arc.end.x,
        arc.end.y,
        arc.center.x - arc.start.x,
        arc.center.y - arc.start.y,
    );

    let rounded_feed_rate = feed_rate_mm_min.round() as i32;
    if active_feed_rate.is_none_or(|active| active != rounded_feed_rate) {
        line.push_str(&format!(" F{rounded_feed_rate}"));
        *active_feed_rate = Some(rounded_feed_rate);
    }

    gcode_lines.push(line);
}

fn push_g5_move(
    gcode_lines: &mut Vec<String>,
    active_feed_rate: &mut Option<i32>,
    feed_rate_mm_min: f32,
    cubic: CubicBezierSegment,
) {
    let mut line = format!(
        "G5 I{:.3} J{:.3} P{:.3} Q{:.3} X{:.2} Y{:.2}",
        cubic.control_a.x - cubic.start.x,
        cubic.control_a.y - cubic.start.y,
        cubic.control_b.x - cubic.end.x,
        cubic.control_b.y - cubic.end.y,
        cubic.end.x,
        cubic.end.y,
    );

    let rounded_feed_rate = feed_rate_mm_min.round() as i32;
    if active_feed_rate.is_none_or(|active| active != rounded_feed_rate) {
        line.push_str(&format!(" F{rounded_feed_rate}"));
        *active_feed_rate = Some(rounded_feed_rate);
    }

    gcode_lines.push(line);
}

fn push_segment(
    segments: &mut Vec<MotionSegment>,
    segment_end_times_s: &mut Vec<f32>,
    start: glam::Vec3,
    end: glam::Vec3,
    kind: MotionKind,
    speed_mm_s: f32,
) {
    let duration_s = start.distance(end) / speed_mm_s.max(1e-3);
    segments.push(MotionSegment { start, end, kind, duration_s });
    let cumulative_s = segment_end_times_s.last().copied().unwrap_or(0.0) + duration_s;
    segment_end_times_s.push(cumulative_s);
}

